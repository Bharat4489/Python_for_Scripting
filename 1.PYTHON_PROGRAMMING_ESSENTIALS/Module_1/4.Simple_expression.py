# Simple Arithmetic Operations in Python

a = 10
b = 5

"""
>>>>>>> SYNTAX  f"some text {expression} more text"
"""
# Addition
addition = a + b
print("Addition:", addition)
print(f"Direct operation: {a} + {b} = {a + b}")
print(f"10 + 5 = {10 + 5}\n")

# Subtraction
subtraction = a - b
print("Subtraction:", subtraction)
print(f"Direct operation: {a} - {b} = {a - b}")
print(f"10 - 5 = {10 - 5}\n")

# Multiplication
multiplication = a * b
print("Multiplication:", multiplication)
print(f"Direct operation: {a} * {b} = {a * b}")
print(f"10 * 5 = {10 * 5}\n")

# Division (floating point)
division = a / b
print("Division:", division)
print(f"10 / 5 = {10 / 5}\n")

# Floor Division (integer division)
floor_division = a // b
print("Floor Division:", floor_division)
print(f"10 // 5 = {10 // 5}\n")

# Modulo (remainder)
modulo = a % b
print("Modulo:", modulo)
print(f"10 % 5 = {10 % 5}\n")

# Exponentiation (power)
exponent = a ** b
print("Exponentiation:", exponent)
print(f"10 ** 5 = {10 ** 5}\n")
print(10**5, "\n") 
